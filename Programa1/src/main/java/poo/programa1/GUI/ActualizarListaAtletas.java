      private void BorrarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BorrarButtonActionPerformed
            // TODO add your handling code here:
            System.out.println(visor.getNombre());
            if (visor == null){
                  return;
            }
            String id = (String) ComboAtletasVisores.getSelectedItem();
            atleta = (Atleta) Persona.List.get(id);
            
            if (atleta == null){
                  System.out.println("hola");
                  return;
            }
            
            int input = JOptionPane.showConfirmDialog(null, "¿Desea borrar?", "Selecione una opción...",
				JOptionPane.YES_NO_OPTION, JOptionPane.ERROR_MESSAGE);
            // 0=sí, 1=no,
            if (input == 0){
                  visor.borrarAtleta(atleta.getId());
                  JOptionPane.showMessageDialog(this, "Atleta borrado existosamente.");
            }
            FillComboAtletasList();
            FillComboAtletasVisores();
      }//GEN-LAST:event_BorrarButtonActionPerformed

