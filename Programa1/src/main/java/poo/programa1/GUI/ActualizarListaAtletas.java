      private void FillComboVisores(){
        Atleta.List.forEach((key, value) -> {
            if (value instanceof Visor){
                ComboVisores.addItem(key);
            }
        });
    } 
      
      private void FillComboAtletasVisores(){
            if (visor == null){
                  return;
            }
            ComboAtletasVisores.removeAllItems();
            for (Atleta atleta : visor.getAtletas()){
                  ComboAtletasVisores.addItem(atleta.getId());
            }
      }
      
      private void FillComboAtletasList(){
            if (visor == null){
                  return;
            }
            ComboAtletasList.removeAllItems();
            Atleta.List.forEach((key, value) -> {
                  if (value instanceof Atleta){
                        if(!visor.getAtletas().contains(value)){
                              ComboAtletasList.addItem(key);
                        }
                  }
            });
      }
      
